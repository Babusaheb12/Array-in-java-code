package array;

public class length_property_1D {
	public static void main(String[] args) {
		
		//length property 1D Array.
		int nums[]= {1,5,8,9,5};
		System.out.println("This is 1D length property");
		System.out.println(nums.length);   
		
		
//		Length property in 2D Array.
		int nums1[][]= {
				{2,5,6,8,},
				{5,9,6,3,},
				{7,8,9,4},
				{1,2,3,5}	
		};
		System.out.println("This is 2D length property");

		System.out.println(nums1[0].length);
	}

}
